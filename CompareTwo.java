public class CompareTwo {
    public static void main(String[] args) {
        int n1 = 10, n2 = 20;
        if (n1 > n2)
            System.out.println(n1 + " is greater than " + n2);
        else if (n1 < n2)
            System.out.println(n2 + " is greater than " + n1);
        else
            System.out.println("Both numbers are equal.");
    }
}
